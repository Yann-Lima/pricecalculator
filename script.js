function totalprice(){
  var area = parseFloat(document.getElementById("area").value);
  var pitch = parseFloat(document.getElementById("pitch").value);
  var stores = parseFloat(document.getElementById("stores").value);
  var typeroof = parseFloat(document.getElementById("typeroof").value);
  var sq = 100;
  var min = 1.15;
  var max = 1.20;

  var pricemin = parseFloat((area * pitch * stores * typeroof)/sq) * min;
  var pricemax = parseFloat((area * pitch * stores * typeroof)/sq) * max;

  priceminimum.innerHTML = ('$' + pricemin.toLocaleString(2))
  pricemaximum.innerHTML = ('$' + pricemax.toLocaleString(2));
}
